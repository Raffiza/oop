class Rekening {
    private String noRekening;
    private int saldo;

    public void setNoRekening(String no){
        this.noRekening = no;
    }

    public String getNoRekening(){
        return this.noRekening;
    }

    public void setSaldo(int saldo){
        this.saldo = saldo;
    }

    public int getSaldo(){
        return this.saldo;
    }
}
